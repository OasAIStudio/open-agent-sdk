#!/usr/bin/env node

const fs = require('fs');
const path = require('path');

const VERSION = '0.1.0-alpha.1';

function printHelp() {
  console.log(`Open Agent SDK CLI v${VERSION}

Usage:
  open-agent-sdk init [project-name] [--force]
  open-agent-sdk --help

Aliases:
  oas init [project-name] [--force]
`);
}

function toKebabCase(input) {
  return input
    .trim()
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '') || 'open-agent-app';
}

function writeFile(targetPath, content, force) {
  if (fs.existsSync(targetPath) && !force) {
    throw new Error(`File already exists: ${targetPath}. Re-run with --force to overwrite.`);
  }

  fs.mkdirSync(path.dirname(targetPath), { recursive: true });
  fs.writeFileSync(targetPath, content, 'utf8');
}

function renderPackageJson(projectName) {
  return `${JSON.stringify({
    name: projectName,
    version: '0.1.0',
    private: true,
    type: 'module',
    scripts: {
      dev: 'tsx src/index.ts',
      build: 'tsc -p tsconfig.json',
      start: 'node dist/index.js',
    },
    dependencies: {
      'open-agent-sdk': '^0.1.0-alpha.1',
    },
    devDependencies: {
      '@types/node': '^20.0.0',
      tsx: '^4.20.0',
      typescript: '^5.3.0',
    },
  }, null, 2)}\n`;
}

function renderTsconfig() {
  return `{
  "compilerOptions": {
    "target": "ES2022",
    "module": "NodeNext",
    "moduleResolution": "NodeNext",
    "strict": true,
    "esModuleInterop": true,
    "skipLibCheck": true,
    "forceConsistentCasingInFileNames": true,
    "outDir": "dist"
  },
  "include": ["src"]
}
`;
}

function renderEnvExample() {
  return `OPENAI_API_KEY=your_api_key_here
# Optional: override default model
# OAS_MODEL=gpt-5.3-codex
`;
}

function renderGitignore() {
  return `node_modules
dist
.env
`;
}

function renderAppSource() {
  return `import { prompt } from "open-agent-sdk";

const model = process.env.OAS_MODEL ?? "gpt-5.3-codex";
const apiKey = process.env.OPENAI_API_KEY;

if (!apiKey) {
  console.error("Missing OPENAI_API_KEY. Copy .env.example to .env and set your key.");
  process.exit(1);
}

const result = await prompt("Read the current directory and summarize key files.", {
  provider: "openai",
  model,
  apiKey,
  allowedTools: ["Glob", "Read"],
});

console.log(result.result);
`;
}

function renderReadme(projectName) {
  return `# ${projectName}

Generated by Open Agent SDK CLI.

## Quick Start

1. Install dependencies:

   npm install

2. Configure env:

   cp .env.example .env

3. Run the agent example:

   npm run dev
`;
}

function createProject(targetDir, force) {
  const projectName = toKebabCase(path.basename(targetDir));

  if (!fs.existsSync(targetDir)) {
    fs.mkdirSync(targetDir, { recursive: true });
  }

  const entries = fs.readdirSync(targetDir);
  if (entries.length > 0 && !force) {
    throw new Error(`Target directory is not empty: ${targetDir}. Use --force to overwrite files.`);
  }

  writeFile(path.join(targetDir, 'package.json'), renderPackageJson(projectName), force);
  writeFile(path.join(targetDir, 'tsconfig.json'), renderTsconfig(), force);
  writeFile(path.join(targetDir, '.env.example'), renderEnvExample(), force);
  writeFile(path.join(targetDir, '.gitignore'), renderGitignore(), force);
  writeFile(path.join(targetDir, 'src', 'index.ts'), renderAppSource(), force);
  writeFile(path.join(targetDir, 'README.md'), renderReadme(projectName), force);

  console.log(`\nInitialized Open Agent SDK template in ${targetDir}\n`);
  console.log('Next steps:');
  console.log(`  cd ${targetDir}`);
  console.log('  npm install');
  console.log('  cp .env.example .env');
  console.log('  npm run dev\n');
}

function main() {
  const args = process.argv.slice(2);

  if (args.length === 0 || args.includes('--help') || args.includes('-h')) {
    printHelp();
    return;
  }

  const command = args[0];

  if (command !== 'init') {
    console.error(`Unknown command: ${command}`);
    printHelp();
    process.exit(1);
  }

  const force = args.includes('--force');
  const targetName = args[1] && !args[1].startsWith('-') ? args[1] : '.';
  const targetDir = path.resolve(process.cwd(), targetName);

  try {
    createProject(targetDir, force);
  } catch (error) {
    console.error(String(error.message || error));
    process.exit(1);
  }
}

main();
